#' The prediction function
#'
#' @param data_train a matrix
#' @param data_test a matrix
#' @return the estimated parameters
#' @examples
#' program(data_train = data_train, data_test = data_test)
program <- function(data_train, data_test) {
    ##
    ## YOUR CODE BEGINS HERE
    ## 
    
    # use more represented categorie as model (NULL model)
    alpha = 1
    lambda = 0.01
    m = glmnet::glmnet(
      x=data.matrix(data_train[,-1]),
      y=data_train[,1],
      family="binomial",
      standardize=TRUE,
      lambda=lambda,
      alpha=alpha
    )
    data_pred  = predict(m, type="class", newx=data.matrix(data_test [,-1]))

